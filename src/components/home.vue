				<el-collapse-item title="2.什么是结构、样式与行为?" name="2">
					<div>简单来说，HTML5是“将设计图转换为用户查看的网页”所需要的技术。 结构实现的是网页中的标题、列表、图片等标签。 样式处理的是标题文字的字体大小、颜色，图片尺寸，某个标签的背景等。 行为可以实现网页中的时间，电商网站当中的倒计时效果，在注册表单时用户名是否重复的检测，网站当中顶部图片的切换特效等等。 简单的理解结构样式和行为：将网页看做一个装修好的功能完备的房子，那么结构实现的是房间以及家具的位置布局，样式则是针对房间进行装饰，行为是为房间添加“开门”等功能。
					</div>

				</el-collapse-item>
				<el-collapse-item title="3. JavaScript是什么?" name="3">
					<div>JavaScript是一种基于对象和事件驱动（Drive）并具有相对安全 性的客户端脚本语言。同时也是一种广泛用于客户端Web开发的脚本语言，常用来给HTML网页增加动态功能，例如响应用户的各种操作。它最初由网景公司的Brendan Eich设计，是一种动态、弱类型、基于原型的语言，内置支持类。JavaScript是Sun公司的注册商标。[1] Ecma国际以JavaScript为基础制定了ECMAScript标准。JavaScript也可以用于其他场合， 如服务器端编程。完整的JavaScript实现包含三个部分：ECMAScript，文档对象模型，字节顺序记号。
					</div>
				</el-collapse-item>
				<el-collapse-item title="4. CSS3是什么?" name="4">
					<div>所谓复合属性并不仅是CSS3才有的，复合属性指的是几种属性结合一起书写的，比如CSS当中的背景 background，其属性值包含“背景颜色、背景图片、背景定位、背景重复”等等 border属性，属性值包含“边框宽度、边框颜色、边框线型” 在CSS3当中，典型的复合属性，包括变形（transform）、过渡“transition”、动画“animation”等 如果是初学HTML与CSS以及HTML5和CSS3，可以使用《HTML5布局之路》书籍学习，里面讲解的很完整，贴合实际前端开发工作
					</div>
				</el-collapse-item>
				<el-collapse-item title="5. react是什么?" name="5">
					<div>用脚本进行DOM操作的代价很昂贵。有个贴切的比喻，把DOM 和JavaScript各自想象为一个岛屿，它们之间用收费桥梁连接，js每次访问 DOM，都要途径这座桥，并交纳“过桥费”,访问DOM的次数越多，费用也就越高。 因 此，推荐的做法是尽量减少过桥的次数，努力待在ECMAScript岛上。因为这个原因react的 虚拟dom就显得难能可贵了，它创造了虚拟dom并且将它们储存起来，每当状态发生变化的时候就会创造 新的虚拟节点和以前的进行对比，让变化的部分进行渲染 。整个过程没有对dom进行获取和操作，只有一个渲染的过程，所以react说是一个ui框架。
					</div>
